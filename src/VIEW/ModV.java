       public void Mostrar(int codigo_id) {

        codigo = codigo_id;
        VendedoresDAORelacional sd = new VendedoresDAORelacional();

        Vendedores Ven = sd.obtener_vendedor(codigo_id);

        JTcodigo.setText(Ven.getCodigo()+ "");
        JTnombre.setText(Ven.getNombre() + "");
        JTcaja.setText(Ven.getCaja()+ "");
        JTventas.setText(Ven.getVentas() + "");
        JTgenero.setText(Ven.getGenero() + "");
        JTcontrase単a.setText(Ven.getPass() + "");
        

    }
            
            
        private void enviarV() {


       Vendedores VE = new Vendedores(Integer.parseInt(JTcodigo.getText()), JTnombre.getText(), Integer.parseInt(JTcaja.getText()), Integer.parseInt(JTventas.getText()), JTgenero.getText(), JTcontrase単a.getText());
       VendedoresDAORelacional VEDAO = new VendedoresDAORelacional();
       VEDAO.modificar_vendedor(VE);


       }
            
     public void cargar(Vendedores vendedor) {
        JTcodigo.setText(String.valueOf(vendedor.getCodigo()));
        JTnombre.setText(vendedor.getNombre());
        JTcaja.setText(String.valueOf(vendedor.getCaja()));
        JTventas.setText(String.valueOf(vendedor.getVentas()));
        JTgenero.setText(vendedor.getGenero());
        JTcontrase単a.setText(vendedor.getPass());
     
       
        codigo_id = vendedor.getCodigo();
    }

